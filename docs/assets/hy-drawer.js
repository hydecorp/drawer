            const velocity$ = translateX$.pipe(timestamp(), pairwise(), filter(([{ timestamp: prevTime }, { timestamp: time }]) => time - prevTime > 0), map(([{ value: prevX, timestamp: prevTime }, { value: x, timestamp: time }]) => (x - prevX) / (time - prevTime)), startWith(0));
